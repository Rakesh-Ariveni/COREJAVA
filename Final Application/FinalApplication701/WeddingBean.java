package com.Evergent.CoreJava.Rakesh.FinalApplication701;

public class WeddingBean {
    private String weddingDate;
    private String brideName;
    private String groomName;
    private String venue;
    private int estimatedGuests;

    // Getters and Setters
    public String getWeddingDate() {
        return weddingDate;
    }
    public void setWeddingDate(String weddingDate) {
        this.weddingDate = weddingDate;
    }
    public String getBrideName() {
        return brideName;
    }
    public void setBrideName(String brideName) {
        this.brideName = brideName;
    }
    public String getGroomName() {
        return groomName;
    }
    public void setGroomName(String groomName) {
        this.groomName = groomName;
    }
    public String getVenue() {
        return venue;
    }
    public void setVenue(String venue) {
        this.venue = venue;
    }
    public int getEstimatedGuests() {
        return estimatedGuests;
    }
    public void setEstimatedGuests(int estimatedGuests) {
        this.estimatedGuests = estimatedGuests;
    }
}